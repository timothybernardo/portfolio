// Run this from your project root:  node generate-photos.js
//
// It scans public/images/ for folders of photos and
// generates src/data/photos.js automatically.

const fs = require("fs");
const path = require("path");

const imagesDir = path.join(__dirname, "public", "images");
const outputFile = path.join(__dirname, "src", "data", "photos.js");

function formatTitle(filename) {
  // "back-bay-sunset.jpg" → "Back Bay Sunset"
  return filename
    .replace(/\.[^.]+$/, "")
    .split("-")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

function formatCategory(folderName) {
  // "new-york" → "New York"
  return folderName
    .split("-")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

const imageExtensions = [".jpg", ".jpeg", ".png", ".webp"];

let photos = [];
let id = 1;

const folders = fs.readdirSync(imagesDir).filter((f) => {
  return fs.statSync(path.join(imagesDir, f)).isDirectory();
});

for (const folder of folders) {
  const cat = formatCategory(folder);
  const folderPath = path.join(imagesDir, folder);
  const files = fs.readdirSync(folderPath).filter((f) => {
    return imageExtensions.includes(path.extname(f).toLowerCase());
  });

  for (const file of files) {
    photos.push({
      id: id++,
      title: formatTitle(file),
      cat,
      img: `/images/${folder}/${file}`,
    });
  }
}

const output = `// Auto-generated by generate-photos.js — ${new Date().toLocaleDateString()}
// To regenerate: node generate-photos.js

const photos = ${JSON.stringify(photos, null, 2)};

export default photos;
`;

fs.writeFileSync(outputFile, output);
console.log(`Generated ${photos.length} photos from ${folders.length} locations.`);
console.log(`Output: ${outputFile}`);